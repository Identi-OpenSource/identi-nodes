function createGradient(color1: string, color2: string) {
  return `linear-gradient(to bottom, ${color1}, ${color2})`;
}
function createRightGradient(color1: string, color2: string) {
  return `linear-gradient(to right, ${color1}, ${color2})`;
}

interface GradientsPaletteOptions {
  primary: string;
  info: string;
  success: string;
  warning: string;
  error: string;
  icon: string;
  additional1: string;
  additional2: string;
  additional3: string;
}

